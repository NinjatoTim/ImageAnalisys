def print_template(template, number):
    """
    Imprime una plantilla de número de forma visual
    """
    print(f"\n=== NÚMERO {number} ===")
    for row in template:
        line = ""
        for pixel in row:
            if pixel == 1:
                line += "██"  # Bloque sólido para 1's
            else:
                line += "  "  # Espacio para 0's
        print(line)

def save_templates_to_file(templates, filename="number_templates.txt"):
    with open(filename, 'w') as f:
        for number, template in templates.items():
            f.write(f"# Plantilla del número {number}\n")
            f.write(f"templates[{number}] = np.array([\n")
            for i, row in enumerate(template):
                f.write("    [")
                for j, pixel in enumerate(row):
                    f.write(str(pixel))
                    if j < len(row) - 1:
                        f.write(",")
                f.write("]")
                if i < len(template) - 1:
                    f.write(",")
                f.write("\n")
            f.write("])\n\n")

