def similitud(img, plantilla):
    total = 0
    coincidencias = 0
    for i in range(len(img)):
        for j in range(len(img[0])):
            total += 1
            if img[i][j] == plantilla[i][j]:
                coincidencias += 1
    return coincidencias / total

def png_a_matriz_binaria(ruta, umbral=128):
    img = Image.open(ruta).convert("L")  # Escala de grises
    img = img.resize((28, 28))  # Por si acaso
    matriz = []
    for y in range(28):
        fila = []
        for x in range(28):
            pixel = img.getpixel((x, y))
            fila.append(0 if pixel < umbral else 1)
        matriz.append(fila)
    return matriz

