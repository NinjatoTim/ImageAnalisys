def cargar_dataset(directorio_base):
    datos = []
    etiquetas = []
    for etiqueta in range(10):
        carpeta = os.path.join(directorio_base, str(etiqueta))
        print("cargando dataset:",etiqueta)
        for archivo in os.listdir(carpeta):
            ruta = os.path.join(carpeta, archivo)
            matriz = png_a_matriz_binaria(ruta)
            vector = np.array(matriz).flatten()
            datos.append(vector)
            etiquetas.append(etiqueta)

    return np.array(datos), np.array(etiquetas)

def activacion(x):
    return 1 if x >= 0 else 0

def predecir(entrada, pesos, bias):
    suma = np.dot(entrada, pesos) + bias
    return activacion(suma)

def entrenar(dataset, etiquetas_binarias, epocas=10, tasa_aprendizaje=0.01):
    pesos = np.random.rand(dataset.shape[1])
    bias = 0
    for _ in range(epocas):
        for i in range(len(dataset)):
            entrada = dataset[i]
            etiqueta = etiquetas_binarias[i]
            pred = predecir(entrada, pesos, bias)
            error = etiqueta - pred
            pesos += tasa_aprendizaje * error * entrada
            bias += tasa_aprendizaje * error
    return pesos, bias

def entrenar_perceptrones_multiclase(dataset, etiquetas):
    clasificadores = []
    for digito in range(10):
        etiquetas_binarias = np.array([1 if e == digito else 0 for e in etiquetas])
        pesos, bias = entrenar(dataset, etiquetas_binarias)
        print("pesos: ",pesos)
        print("bias: ",bias)
        clasificadores.append((pesos, bias))
    return clasificadores

def predecir_digito(vector, clasificadores):
    salidas = [np.dot(vector, pesos) + bias for pesos, bias in clasificadores]
    return np.argmax(salidas)  # el que tenga la mayor activación (no binaria)


